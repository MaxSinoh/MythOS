#include <asm/idt/idt.h>

#define IDT_GATE(num, selector) \
    ((unsigned long)selector << 16 | (unsigned long)(num) << 8 | 0x8E)

void initIDT(void) {
    unsigned long idt[256];
    int i;

    for (i = 0; i < 256; i++) {
        idt[i] = IDT_GATE(i, 0x08);
    }

    __asm__ volatile("lidt (%0)" ::"r"(idt));
}
